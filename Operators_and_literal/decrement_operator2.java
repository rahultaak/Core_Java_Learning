package Operators_and_literal;

public class decrement_operator2 {
    public static void main(String[] args) {
        int b = 20;
        System.out.println(--b + b--);
        System.out.println(b);
        int c = 10;
        System.out.println(--c + c++ + ++c);
        System.out.println(c);



    }
}
