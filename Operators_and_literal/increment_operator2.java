package Operators_and_literal;

public class increment_operator2 {
    public static void main(String[] args) {

        int a = 20;
        System.out.println(++a + a++ + a++);
        System.out.println(a);

        int b = 10;
        System.out.println(++b + b++);
        System.out.println(b);



    }
}
