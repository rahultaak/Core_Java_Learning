package data_type;

public class primitive_data_type {
    public static void main(String[] args) {
        byte b=10;
        short s=2;
        int num = 234;
        long l1 = 132343242L;
        float f1 = 24.342f;
        double d=23.534533;
        char C = 'A';
        char _4 = 'C';
        System.out.println("the byte is " +b);
        System.out.println("the short is " +s);
        System.out.println("the int is " +num);
        System.out.println("the long is " +l1);
        System.out.println("the float is " +f1);
        System.out.println("the double is " +d);
        System.out.println("the char is " +C);
        System.out.println("the char is " +_4);


    }
}
